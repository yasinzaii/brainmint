"""Diffusion metrics utilities.

This module is designed for diffusion model evaluation where you have:
- A *real* test dataset (reference distribution)
- A *synthetic* dataset generated by a diffusion sampler (generated distribution)
- Per-sample modality metadata (e.g., t1w, t2w, flair, ...)

Metrics:
- FID on deep feature vectors (e.g., MedicalNet ResNet50 features).
- MMD on the same deep feature vectors.
- MS-SSIM *diversity* on synthetic samples *within each modality*:
    We compute MS-SSIM on randomly sampled pairs of synthetic images, grouped by modality.
    Lower MS-SSIM indicates higher diversity.

Notes:
- FID expects (N, D) features.
- MMD requires y_pred and y to have the same shape (we subsample to match).
- Optional intensity normalisation (minmax or zscore) can be applied inside the
  calculator *before* feature extraction.
"""

import math
from collections import defaultdict
from typing import Any, Dict, Iterable, List, Mapping, MutableMapping, Sequence, Tuple

import numpy as np
import torch
from medmetric.metrics import FID, MMD, MS_SSIM
from monai.utils import MetricReduction

try:
    from tqdm.auto import tqdm
except ImportError:  # pragma: no cover - exercised only without optional extra

    def tqdm(iterable, *args, **kwargs):
        return iterable

